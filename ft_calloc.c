#include "libft.h"

void	*ft_calloc(size_t nelem, size_t elsize)
{
	void	*ptr;
	size_t	size;

	size = nelem * elsize;
	ptr = malloc(size);
	ft_bzero(ptr, size);
	return (ptr);
}
